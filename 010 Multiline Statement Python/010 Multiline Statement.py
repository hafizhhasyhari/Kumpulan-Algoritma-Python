# Multiline Statement

# Contoh kelanjutan baris implisit
x = ('1' + '2' + '3' + '4' + '5' +
    '6' + '7')

# Contoh kelanjutan baris eksplisit
y = '0' + '8' + '5' + '7' + '6' + '5' + '4' + '3' + '3' +\
    '7' + '5' + '3'

hari_kerja = ['Senin', 'Selasa', 'Rabu',
              'Kamis', 'Jumat']

akhir_pekan = {'Sabtu',
               'Ahad'}

print ('Nilai x adalah: ', x)
print ('Nilai y adalah: ', y)
print ('Hari kerja adalah: ', hari_kerja)
print ('Akhir pekan adalah: ', akhir_pekan)
